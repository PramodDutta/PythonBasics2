# Lambda Expression In python

# A lambda is an anonymous function that returns some form of data.

def sum_of_two_numbers(a, b):
    return a + b


result = sum_of_two_numbers(4, 5)
print(result)


# anonymous - Have no name - Coding
# lambda

def mul3(num):
    return num * 3


result2 = mul3(9)
print(result2)

final_result = lambda num: num+1
